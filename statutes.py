def search_statutes(keywords, top_k=10):
    """
    Search across all statute databases for matching keywords.

    Args:
        keywords: List of Chinese keyword strings to search for
        top_k: Maximum number of results to return

    Returns:
        List of dicts with keys: 'statute', 'chapter', 'article', 'summary'
    """
    if not keywords:
        return []
    results = []

    databases = {
        '民法典': CIVIL_CODE,
        '公司法': COMPANY_LAW,
        '劳动合同法': LABOR_LAW,
        '民事诉讼法': CIVIL_PROCEDURE_LAW,
        '刑法': CRIMINAL_LAW,
    }

    def search_in(source_name, source_data):
        if 'chapters' in source_data:
            for ch_name, ch_data in source_data['chapters'].items():
                for art, summary in ch_data.get('key', {}).items():
                    text_to_search = f"{art} {summary} {ch_name} {source_name}"
                    for kw in keywords:
                        if kw in text_to_search:
                            results.append({
                                'statute': source_name,
                                'chapter': ch_name,
                                'article': art,
                                'summary': summary,
                                'match_kw': kw,
                            })
                            break

    for db_name, db_data in databases.items():
        if db_name == '民法典':
            for volume_name, volume_data in db_data.items():
                search_in(f"民法典·{volume_data['title']}", volume_data)
        elif db_name in ('劳动合同法', '民事诉讼法', '刑法'):
            for category, provisions in db_data.get('key_provisions', {}).items():
                for art, summary in provisions.items():
                    for kw in keywords:
                        if kw in f"{art} {summary} {category}":
                            results.append({
                                'statute': db_name,
                                'chapter': category,
                                'article': art,
                                'summary': summary,
                                'match_kw': kw,
                            })
                            break
        else:
            search_in(f"{db_data.get('title', db_name)}", db_data)

    # Search local regulations (江浙沪)
    for region, region_data in LOCAL_REGULATIONS.items():
        for law_name, law_data in region_data['laws'].items():
            for art_key, summary in law_data.get('key', {}).items():
                text_to_search = f"{art_key} {summary} {law_name} {region} {region_data['title']}"
                for kw in keywords:
                    if kw in text_to_search:
                        results.append({
                            'statute': law_name,
                            'chapter': f"{region}·{law_data.get('type', '')}（{law_data.get('effective', '')}）",
                            'article': art_key,
                            'summary': summary,
                            'match_kw': kw,
                        })
                        break

    # Deduplicate and sort by match count
    seen = set()
    unique_results = []
    for r in results:
        key = (r['statute'], r['article'])
        if key not in seen:
            seen.add(key)
            unique_results.append(r)

    return unique_results[:top_k]


def get_article_text(statute_name, article_number):
    """
    Look up a specific article by statute name and article number.

    Args:
        statute_name: e.g. '民法典', '公司法', '劳动合同法', '民事诉讼法', '刑法'
        article_number: e.g. '第143条', '第584条', or just '143', '584'

    Returns:
        Article summary string, or None if not found.
    """
    # Normalize article number
    if article_number is None:
        return None

    if isinstance(article_number, int):
        art_str = f"第{article_number}条"
        art_num_str = str(article_number)
    elif not article_number.startswith('第'):
        art_str = f"第{article_number}条"
        art_num_str = str(article_number)
    else:
        art_str = article_number
        m = re.search(r'(\d+)', art_str)
        article_number = int(m.group(1)) if m else 0
        art_num_str = str(article_number)

    databases = {
        '民法典': CIVIL_CODE,
        '公司法': COMPANY_LAW,
        '劳动合同法': LABOR_LAW,
        '民事诉讼法': CIVIL_PROCEDURE_LAW,
        '刑法': CRIMINAL_LAW,
    }

    if statute_name not in databases:
        # Try local regulations — use exact key match, not substring
        for region, region_data in LOCAL_REGULATIONS.items():
            for law_name, law_data in region_data['laws'].items():
                if statute_name == law_name:
                    for k, summary in law_data.get('key', {}).items():
                        # Extract article number from the key ("第3条"→"3", "通知要点1"→"通知要点1")
                        key_num = k.replace('第', '').replace('条', '')
                        if art_num_str == key_num or art_str == k:
                            return f"【{region}·{law_name}（{law_data.get('effective', '')}）】{k}：{summary}"
                    if art_str in law_data.get('key', {}):
                        return f"【{region}·{law_name}（{law_data.get('effective', '')}）】{art_str}：{law_data['key'][art_str]}"
        return None

    db = databases[statute_name]

    if statute_name == '民法典':
        for vol_name, vol_data in db.items():
            for ch_name, ch_data in vol_data.get('chapters', {}).items():
                if art_str in ch_data.get('key', {}):
                    return f"【{vol_data['title']}·{ch_name}】{art_str}：{ch_data['key'][art_str]}"
    elif statute_name in ('劳动合同法', '民事诉讼法', '刑法'):
        for cat, provisions in db.get('key_provisions', {}).items():
            if art_str in provisions:
                return f"【{cat}】{art_str}：{provisions[art_str]}"
    else:
        for ch_name, ch_data in db.get('chapters', {}).items():
            if art_str in ch_data.get('key', {}):
                return f"【{ch_name}】{art_str}：{ch_data['key'][art_str]}"

    return None


def get_chapter_outline(statute_name):
    """
    Return the full chapter/article outline of a statute.

    Args:
        statute_name: '民法典', '公司法', etc.

    Returns:
        Formatted outline string.
    """
    databases = {
        '民法典': CIVIL_CODE,
        '公司法': COMPANY_LAW,
        '劳动合同法': LABOR_LAW,
        '民事诉讼法': CIVIL_PROCEDURE_LAW,
        '刑法': CRIMINAL_LAW,
    }

    if statute_name not in databases:
        # Check local regulations
        for region, region_data in LOCAL_REGULATIONS.items():
            for law_name, law_data in region_data['laws'].items():
                if statute_name == law_name:
                    lines = [f"《{law_name}》（{law_data.get('effective', '')}）", f"地域：{region}，类型：{law_data.get('type', '')}", "=" * 50]
                    for k, v in law_data.get('key', {}).items():
                        lines.append(f"  {k}: {v}")
                    return '\n'.join(lines)
        return f"未找到法规：{statute_name}"

    db = databases[statute_name]
    lines = [f"{statute_name}篇章索引", "=" * 50]

    if statute_name == '民法典':
        for vol_name, vol_data in db.items():
            lines.append(f"\n{vol_data['title']}（{vol_data['articles']}）")
            for ch_name, ch_data in vol_data.get('chapters', {}).items():
                r = ch_data['range']
                lines.append(f"  ├─ {ch_name}（第{r[0]}-{r[1]}条）")
    elif statute_name in ('劳动合同法', '民事诉讼法', '刑法'):
        for cat in db.get('key_provisions', {}):
            lines.append(f"\n{cat}")
            for art in db['key_provisions'][cat]:
                lines.append(f"  {art}")
    else:
        for ch_name, ch_data in db.get('chapters', {}).items():
            r = ch_data.get('range', ('?', '?'))
            lines.append(f"\n{ch_name}（第{r[0]}-{r[1]}条）")
            for art in ch_data.get('key', {}):
                lines.append(f"  {art}")

    return '\n'.join(lines)


